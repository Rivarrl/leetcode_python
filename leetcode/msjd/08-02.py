from algorithm_utils import *

class Solution:
    """
    [面试题 08.02. 迷路的机器人](https://leetcode-cn.com/problems/robot-in-a-grid-lcci/)
    """
    @timeit
    def pathWithObstacles(self, obstacleGrid: List[List[int]]) -> List[List[int]]:
        # bfs
        if obstacleGrid[0][0] == 1 or obstacleGrid[-1][-1] == 1: return []
        from collections import deque
        r, c = len(obstacleGrid), len(obstacleGrid[0])
        stk = deque([[0,0,[[0,0]]]])
        seen = [[0] * c for _ in range(r)]
        seen[0][0] = 1
        while stk:
            i, j, path = stk.pop()
            if i == r-1 and j == c-1:
                return path
            for x, y in ((i+1, j), (i, j+1)):
                if x < r and y < c and obstacleGrid[x][y] == 0 and seen[x][y] == 0:
                    seen[x][y] = 1
                    stk.appendleft([x, y, path+[[x, y]]])
        return []

    @timeit
    def pathWithObstacles2(self, obstacleGrid: List[List[int]]) -> List[List[int]]:
        # dfs
        if obstacleGrid[0][0] == 1 or obstacleGrid[-1][-1] == 1: return []
        r, c = len(obstacleGrid), len(obstacleGrid[0])
        seen = [[0] * c for _ in range(r)]
        res = []
        def f(i, j, path):
            if i == r or j == c: return
            if i == r - 1 and j == c - 1:
                nonlocal res
                res = path
                return
            if seen[i][j] == 1: return
            seen[i][j] = 1
            for x, y in ((i+1, j), (i, j+1)):
                if x < r and y < c and obstacleGrid[x][y] == 0 and seen[x][y] == 0:
                    f(x, y, path+[[x, y]])
        f(0, 0, [[0, 0]])
        return res

